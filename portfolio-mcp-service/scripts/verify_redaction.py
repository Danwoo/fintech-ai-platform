"""계좌·카드 식별자 redaction 회귀 검증 — redact_secrets 계약.

계약:
  (1) 라벨(account_no/계좌번호/카드번호…)+값은 **값 전체**가 마스킹된다. 구분자는 :/=/전각 콜론(：)/공백
      모두 받고, 값이 공백으로 쪼개져도('5012 01 2345678') 꼬리가 남지 않는다.
      마커 존재가 아니라 **원문 숫자가 하나도 남지 않음**으로 검사한다 — 부분 마스킹 + 꼬리 누출 검출.
  (2) 라벨 없는 bare 계좌·카드는 가운데만 가리는 부분 마스킹이 계약 — 원문 토큰 전체가 남으면 안 된다.
      한글 인접('계좌5012-01-2345678') 에서도 경계가 잡힌다.
  (3) 오탐 금지 — 라벨 뒤 값이 숫자형이 아니면(예: 'account_no is required', '계좌번호 확인 요망')
      건드리지 않는다. 라벨 없는 무관 텍스트(종목명·수량·코드)도 그대로.
      YYYY-MM-DD 날짜(예: '회사채 2024-05-15 만기')는 bare 계좌 패턴이 오탐하지 않는다.
  (4) 값 뒤에 이어지는 무관한 티커·수량·금액·날짜는 보존된다 (#37 "날짜·금액은 기능 데이터" 계약).
      개행 너머 다음 줄의 선두 숫자도, 같은 줄의 다음 토큰도 삼키지 않는다.
      **알려진 예외** — 공백 분할된 값 뒤의 **맨숫자 토큰**(구두점·단위·글자가 안 붙은 것)은 모양상 계좌
      조각과 구분되지 않아 흡수된다(#58 에서 알려진 채로 수용). 조각 길이 상한을 푼 뒤(#59) 그 흡수 범위에
      `2024-05-15` 형태 날짜가 포함된다 — 점 구분 날짜(`2024.05.15`)·금액·수량은 계속 보존된다.
  (5) 긴 공백 런 입력에서 백트래킹이 폭주하지 않는다 (ReDoS).

순수 함수 검사 — DB/LLM/외부 API 불필요. `uv run python scripts/verify_redaction.py` (cwd=서비스 루트).
"""

from __future__ import annotations

import sys
import time
from pathlib import Path

sys.path.insert(0, str(Path(__file__).resolve().parent.parent / "app"))

from utils.redaction.redactor import redact_secrets  # noqa: E402

_MASK_MARK = "가려짐"


def main() -> int:
    problems: list[str] = []

    # (1) 라벨+값 — 값 전체 마스킹. 값 밖에는 숫자를 두지 않은 케이스만 넣어,
    #     "출력에 숫자가 하나라도 남으면 부분 누출" 로 판정할 수 있게 한다.
    must_mask_whole = [
        "account_no 5012012345678",  # 공백 구분 + 연속 13자리
        "account_no: 5012-01-2345678",  # 기존 :/= KV
        "계좌번호 501201-23-45678",  # 공백 + 하이픈 값
        "카드번호 5012 0123 4567 8901",  # 공백 카드 16자리
        "account_no 5012 01 2345678",  # 값 내부 공백 분할 (#40)
        "계좌번호 501201 23 45678",  # 값 내부 공백 분할 — 꼬리 '23 45678' 누출 (#40)
        "계좌번호： 5012-01-2345678",  # 전각 콜론 (#40)
        "계좌번호는 5012012345678",  # 라벨 뒤 조사(는) — 한글 인접 \b 불발 (#52)
        "내계좌번호: 5012012345678",  # 라벨 앞 접두(내) — 한글 인접 \b 불발 (#52)
        "계좌번호부터 5012012345678",  # 라벨 뒤 보조사(부터) (#52)
        "계좌번호와 5012012345678",  # 접속조사(와) 뒤 13자리 실계좌 — 종목코드 가드가 실누출을 안 깬다 (#99)
        "계좌번호 1234567",  # 라벨+공백 뒤 7자리 — 6자리 초과라 여전히 계좌로 마스킹 (#99)
        # 공백 분할 값의 **이어지는 조각이 10자리 이상** — 조각당 9자 상한 바로 바깥으로 빠져나가
        # 값 전체가 미마스킹되던 누출 (#59). 조각 길이가 얼마든 마스킹돼야 한다.
        "계좌번호 5012 0123456789",  # 둘째 조각 10자리 (#59 재현 케이스)
        "계좌번호 5012 01234567890123",  # 둘째 조각 14자리 (#59)
        "account_no 5012 0123456789",  # 영문 라벨 경로 (#59)
        "계좌번호: 5012 0123456789",  # 콜론 구분자 경로 — `\S+` 대체가 첫 조각만 먹던 누출 (#59)
        "계좌번호 501201 2345678901",  # 첫 조각 6자리 + 둘째 10자리 (#59)
        "계좌번호 12 345678901234567890",  # 둘째 조각 20자리 — 상한이 없음을 못박는다 (#59)
    ]
    for c in must_mask_whole:
        out = redact_secrets(c)
        if _MASK_MARK not in out:
            problems.append(f"미마스킹(누수): {c!r} → {out!r}")
        elif any(ch.isdigit() for ch in out):
            problems.append(f"부분 마스킹(꼬리 누출): {c!r} → {out!r}")

    # (2) 라벨 없는 bare 하이픈 계좌 — 부분 마스킹(앞2·뒤2 그룹 유지)이 계약이므로 원문 토큰 전체가 사라졌는지만 본다
    must_mask_partial = [
        ("출금 계좌 5012-02-7654321 로 이체", "5012-02-7654321"),  # 문맥 속 하이픈 계좌
        ("계좌5012-01-2345678", "5012-01-2345678"),  # 한글 인접 — \b 불발 (#40)
    ]
    for c, raw_token in must_mask_partial:
        out = redact_secrets(c)
        if _MASK_MARK not in out:
            problems.append(f"미마스킹(누수): {c!r} → {out!r}")
        elif raw_token in out:
            problems.append(f"원문 토큰 잔존: {c!r} → {out!r}")

    # (2b) 라벨 없는 bare 카드 16자리 — 계좌와 달리 **전액** 마스킹이 계약. 앞2·뒤2 그룹을 남기면
    #      16자 중 8자(앞4·뒤4)가 새므로(#52), 출력에 숫자가 하나라도 남으면 부분 마스킹 누출로 판정한다.
    must_mask_card_full = [
        "카드5012 0123 4567 8901",  # 한글 인접 4-4-4-4 공백 구분 — \b 불발 (#52)
        "카드번호5012 0123 4567 8901",  # 라벨 직접인접(구분자 없음) → bare 카드 경로 (#52)
        "카드1234-5678-9012-3456",  # 하이픈 구분 16자리
        "카드5012012345678901",  # 구분자 없는 연속 16자리
    ]
    for c in must_mask_card_full:
        out = redact_secrets(c)
        if _MASK_MARK not in out:
            problems.append(f"카드 미마스킹(누수): {c!r} → {out!r}")
        elif any(ch.isdigit() for ch in out):
            problems.append(f"카드 부분 마스킹(앞/뒤 그룹 누출): {c!r} → {out!r}")

    # (3) 마스킹되면 안 되는 것 — 라벨 뒤 비숫자값, 라벨 없는 무관 텍스트
    must_keep = [
        "account_no is required",
        "계좌번호 확인 요망",
        "보유수량 300주",
        "삼성전자 005930 보유",
        "회사채 2024-05-15 만기",  # YYYY-MM-DD 날짜는 계좌가 아니다
        "회사채 2024-05-15만기",  # 한글 인접 날짜 — lookaround 전환에도 오탐 없음
        "계좌번호는 중요합니다",  # 라벨+조사 뒤 비숫자 — 조사 흡수가 과마스킹으로 새지 않는다 (#52)
        "계좌번호의 종류",  # 라벨+조사(의) 뒤 비숫자 (#52)
        "가상계좌번호안내 005930",  # 라벨+비조사 한글 뒤 종목코드 — 조사 열거가 임의 한글로 안 샌다 (#52)
        "계좌번호와 005930",  # 라벨+접속조사(와) 뒤 6자리 종목코드 — bare 분기가 종목코드를 계좌로 오탐 안 함 (#99)
        "계좌번호 005930",  # 라벨+공백 뒤 6자리 종목코드 — 정확히 6자리 순수 숫자는 계좌 bare 값 아님 (#99)
        "관심종목은 계좌번호와 005930 입니다",  # 문맥 속 조사+종목코드 (#99)
        "계좌번호 123456",  # 라벨+공백 뒤 6자리 종목코드형 (#99)
    ]
    for c in must_keep:
        if _MASK_MARK in redact_secrets(c):
            problems.append(f"오탐(과마스킹): {c!r} → {redact_secrets(c)!r}")

    # (4) 값 뒤 무관 토큰 보존 — KV 값 마스킹이 날짜·다음 줄 수치를 삼키지 않는다
    must_preserve = [
        ("account_no: 5012012345678 2024-05-15 만기", "2024-05-15 만기"),
        ("계좌번호: 5012012345678\n2024-05-15 개설", "2024-05-15 개설"),
        ("계좌번호: 5012012345678\n300 잔고", "300 잔고"),
        # 같은 줄에 이어지는 티커·수량·금액·점 구분 날짜 — 잘린 잔해도 남기지 않는다.
        # 마스킹 과잉이 아니라 **틀린 숫자**가 되는 축이라 별도로 못박는다
        # (잔고 120만 원이 ',200,000' 으로 리포트·LLM 컨텍스트에 들어가면 안 된다).
        ("계좌번호 5012-01-2345678 005930 삼성전자 300주 매수", "005930 삼성전자 300주 매수"),
        ("계좌번호: 5012012345678 300주 보유", "300주 보유"),
        ("account_no: 5012012345678 1,000,000원", "1,000,000원"),
        ("계좌번호: 5012012345678 2024.05.15 만기", "2024.05.15 만기"),
        ("계좌번호: 5012012345678 1,200,000 원", "1,200,000 원"),
        ("account_no: 5012012345678 5.5% 수익률", "5.5% 수익률"),
        ("계좌번호: 5012012345678 12,345,678 원", "12,345,678 원"),  # 자릿수 구분 반복
        ("계좌번호 5012 01 2345678 1,200,000 원", "1,200,000 원"),  # 값이 쪼개진 채로 금액이 이어질 때
        # 조각 길이 상한 해제(#59) 가 정상 포맷을 뭉개지 않는지 — 10자리+ 조각을 흡수한 **뒤에도**
        # 금액·수량·퍼센트·점 구분 날짜·다음 줄은 `_FRAGMENT_END` 가 그대로 막는다.
        ("계좌번호 5012 0123456789 300주 보유", "300주 보유"),  # 글자 인접 수량 (#59)
        ("계좌번호 5012 0123456789 1,200,000 원", "1,200,000 원"),  # 자릿수 구분 금액 (#59)
        ("계좌번호 5012 0123456789 5.5% 수익률", "5.5% 수익률"),  # 소수점 (#59)
        ("계좌번호 5012 0123456789 2024.05.15 만기", "2024.05.15 만기"),  # 점 구분 날짜 (#59)
        ("계좌번호 5012 0123456789\n300 잔고", "300 잔고"),  # 개행 경계 (#59)
        # 첫 조각의 9자 상한 유지를 못박는다 — 상한을 풀면 완성된 하이픈 계좌(15자 런)가 '첫 조각'이 되어
        # 반복부가 뒤 종목코드까지 삼킨다. 위 113행 케이스와 같은 축의 최소 형태 (#59).
        ("계좌번호 5012-01-2345678 005930", "005930"),
        # 분할 값 뒤 **하이픈 날짜**는 보존한다 — `_FRAGMENT_END`(글자·콤마)로는 못 막는 유일한 축이라
        # 조각 앞 `(?!_DATE_AHEAD)` 가 지킨다. 이게 빠지면 만기일 같은 기능 데이터가 소멸한다 (#58/#59 정책:
        # "가림 우선 + 정상 포맷 보존" — 날짜는 정상 포맷이다).
        ("계좌번호 5012 01 2345678 2024-05-15 만기", "2024-05-15 만기"),
        ("계좌번호 5012 0123456789 2024-05-15 만기", "2024-05-15 만기"),
    ]
    for c, kept in must_preserve:
        out = redact_secrets(c)
        if kept not in out:
            problems.append(f"과잉 흡수(기능 데이터 파괴): {c!r} → {out!r}")

    # (5) 백트래킹 폭주 방지 — 긴 공백 런에서 라벨 뒤 조건이 실패해도 선형이어야 한다.
    #     공백을 2배로 늘렸을 때 시간이 4배로 뛰면(2차식) 폭주.
    small = "계좌번호" + " " * 10000 + "5"
    large = "계좌번호" + " " * 40000 + "5"
    elapsed = []
    for probe in (small, large):
        started = time.perf_counter()
        redact_secrets(probe)
        elapsed.append(time.perf_counter() - started)
    if elapsed[1] > elapsed[0] * 8:
        problems.append(f"백트래킹 폭주: 공백 10000자 {elapsed[0] * 1000:.1f}ms → 40000자 {elapsed[1] * 1000:.1f}ms")

    # (5b) 조각 길이 상한을 푼 뒤(#59) 의 반복부 — 긴 숫자 조각이 다수 이어지고 **마지막에 실패**하는
    #      입력(끝이 글자)에서도 선형이어야 한다. 상한 없는 반복 안의 반복은 폭주의 전형적 모양이라 못박는다.
    frag_small = "계좌번호 " + "1234567890 " * 2000 + "x"
    frag_large = "계좌번호 " + "1234567890 " * 8000 + "x"
    frag_elapsed = []
    for probe in (frag_small, frag_large):
        started = time.perf_counter()
        redact_secrets(probe)
        frag_elapsed.append(time.perf_counter() - started)
    if frag_elapsed[1] > frag_elapsed[0] * 16:  # 입력 4배 → 시간 16배 초과면 초선형
        problems.append(
            f"조각 반복 백트래킹 폭주: 조각 2000개 {frag_elapsed[0] * 1000:.1f}ms → 8000개 {frag_elapsed[1] * 1000:.1f}ms"
        )

    if problems:
        print("redaction 위반:")
        for p in problems:
            print(f"  - {p}")
        return 1
    print(
        "redaction OK — 라벨+값 전체 마스킹(꼬리 누출 없음) · bare 부분 마스킹 · 오탐 없음 · 값 뒤 토큰 보존 · 백트래킹 선형"
    )
    return 0


if __name__ == "__main__":
    sys.exit(main())
